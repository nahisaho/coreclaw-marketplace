def _normalize_subskills(payload: dict, group_name: str, group_title: str) -> list[dict[str, str]]:
    raw = payload.get("subskills") or payload.get("sub_skills") or payload.get("sub_agents") or []
    profile = _suite_profile(payload)
    normalized: list[dict[str, str]] = []
    for index, item in enumerate(raw, start=1):
        if isinstance(item, str):
            skill_name = _slugify(item)
            title = _display_name(skill_name)
            description = _default_subskill_description(title, group_title)
        elif isinstance(item, dict):
            skill_name = _slugify(_stringify(item.get("name") or item.get("skill_name") or item.get("title") or f"{group_name}-specialist-{index}"))
            title = _stringify(item.get("title")) or _display_name(skill_name)
            description = _stringify(item.get("description")) or _default_subskill_description(title, group_title)
        else:
            continue
        normalized.append({
            "name": skill_name,
            "title": title,
            "description": description,
        })
    return normalized or _default_subskill_specs(group_name, group_title, profile)


def _render_single_files(payload: dict) -> dict[str, str]:
    skill_name = _slugify(payload.get("skill_name") or payload.get("name") or "generated-skill")
    title = _stringify(payload.get("title")) or _display_name(skill_name)
    version = _normalize_version(payload.get("version"))
    description = _default_description(payload, title)
    entrypoint = _stringify(payload.get("entrypoint")) or "main.py"
    skill_md = _render_single_skill_md(skill_name, title, version, description)
    return {
        "skill.json": _render_skill_json(skill_name, version, description, entrypoint),
        entrypoint: _render_main_py(skill_name, version, description),
        "README.md": _render_single_readme(title, description),
        "SKILL.md": skill_md,
        "source/SKILL.md": skill_md,
    }


def _render_suite_files(payload: dict) -> tuple[dict[str, str], dict[str, object]]:
    group_name = _slugify(payload.get("group_name") or payload.get("skill_name") or payload.get("name") or "generated-group")
    group_title = _stringify(payload.get("group_title") or payload.get("title")) or _display_name(group_name)
    version = _normalize_version(payload.get("version"))
    root_skill_name = _slugify(payload.get("root_skill_name") or f"{group_name}-assistant")
    orchestrator_name = _slugify(payload.get("orchestrator_name") or f"{group_name}-orchestrator")
    root_description = _default_description(payload, group_title)
    group_description = _stringify(payload.get("group_description")) or _default_group_description(group_title, root_description)
    group_icon = _stringify(payload.get("group_icon")) or DEFAULT_GROUP_ICON
    suite_profile = _suite_profile(payload)
    orchestrator_contracts = _orchestrator_profile_contracts(group_title, suite_profile)
    subskills = _normalize_subskills(payload, group_name, group_title)

    files: dict[str, str] = {
        "group.json": _render_group_json(group_title, group_description, group_icon, 2 + len(subskills)),
    }

    root_skill_md = _render_suite_root_skill_md(
        root_skill_name,
        group_name,
        group_title,
        version,
        root_description,
        orchestrator_name,
        subskills,
    )
    files.update(
        _render_package(
            "",
            root_skill_name,
            group_title,
            version,
            root_description,
            root_skill_md,
            _render_suite_root_readme(root_skill_name, group_name, version, orchestrator_name, subskills),
            ["artifact-output", "verification-loop", "suite-root", "orchestration-ready"],
        )
    )

    orchestrator_description = _stringify(payload.get("orchestrator_description")) or _default_orchestrator_description(group_title)
    flow = [item["name"] for item in subskills]
    files.update(
        _render_package(
            orchestrator_name,
            orchestrator_name,
            _display_name(orchestrator_name),
            version,
            orchestrator_description,
            _render_orchestrator_skill_md(
                orchestrator_name,
                _display_name(orchestrator_name),
                orchestrator_description,
                flow,
                orchestrator_contracts["input_contract"],
                orchestrator_contracts["output_contract"],
                orchestrator_contracts["quality_gates"],
                orchestrator_contracts["fallback_policy"],
                version,
            ),
            _render_subskill_readme(_display_name(orchestrator_name), orchestrator_description, "Routes and validates suite execution across sub-skills."),
            ["artifact-output", "verification-loop", "orchestrator", "suite-routing"],
        )
    )

    for item in subskills:
        skill_md = _render_subskill_skill_md(item["name"], item["title"], version, item["description"], group_title)
        files.update(
            _render_package(
                item["name"],
                item["name"],
                item["title"],
                version,
                item["description"],
                skill_md,
                _render_subskill_readme(item["title"], item["description"], f"Handles the {item['title'].lower()} step in the suite."),
                ["artifact-output", "verification-loop", "specialized-subskill"],
            )
        )

    metadata = {
        "structure_type": "suite",
        "suite_profile": suite_profile,
        "group_name": group_name,
        "group_title": group_title,
        "root_skill_name": root_skill_name,
        "orchestrator_name": orchestrator_name,
        "subskills": [item["name"] for item in subskills],
        "orchestrator_contracts": orchestrator_contracts,
    }
    return files, metadata


def _should_generate_template(payload: dict, explicit_files: dict[str, str]) -> bool:
    if payload.get("generate_template") is not None:
        return bool(payload.get("generate_template"))
    if explicit_files:
        return True
    return any(payload.get(key) for key in ("skill_name", "name", "description", "true_objective", "surface_request", "user_request", "group_name"))


def _build_files(payload: dict) -> tuple[dict[str, str], list[str], dict[str, object]]:
    explicit_files = dict(payload.get("files") or {})
    generated_files: list[str] = []
    template_meta = {"structure_type": "single"}
    if not _should_generate_template(payload, explicit_files):
        return explicit_files, generated_files, template_meta

    if _suite_requested(payload, explicit_files):
        defaults, template_meta = _render_suite_files(payload)
    else:
        defaults = _render_single_files(payload)

    for relative_path, content in defaults.items():
        if relative_path not in explicit_files:
            explicit_files[relative_path] = content
            generated_files.append(relative_path)
    return explicit_files, generated_files, template_meta


def _is_within_workspace(path: Path) -> bool:
    try:
        path.resolve().relative_to(WORKSPACE_ROOT.resolve())
        return True
    except ValueError:
        return False


def _default_output_name(input_data: dict) -> str:
    if _suite_requested(input_data, dict(input_data.get("files") or {})):
        return _slugify(input_data.get("group_name") or input_data.get("skill_name") or input_data.get("name") or "generated-group")
    return _slugify(input_data.get("skill_name") or input_data.get("name") or SKILL_NAME)


def _resolve_output_dir(input_data: dict) -> tuple[Path, str | None]:
    default_dir = _artifact_dir(_default_output_name(input_data))
    requested = input_data.get("output_dir")
    if not requested:
        return default_dir, None

    candidate = Path(requested)
    candidate = candidate if candidate.is_absolute() else (WORKSPACE_ROOT / candidate)
    if _is_within_workspace(candidate):
        return candidate, None

    return default_dir, f"Requested output_dir '{requested}' is outside the workspace; saved to artifact-visible path instead."


def _write_files(output_dir: Path, files: dict[str, str]) -> list[str]:
    written_files: list[str] = []
    for relative_path, content in files.items():
        destination = output_dir / relative_path
        destination.parent.mkdir(parents=True, exist_ok=True)
        destination.write_text(content, encoding="utf-8")
        written_files.append(str(destination.relative_to(WORKSPACE_ROOT)))
    return sorted(written_files)


def _zip_directory(output_dir: Path) -> dict:
    zip_path = output_dir.with_suffix(".zip")
    files_packed = 0
    with ZipFile(zip_path, "w", compression=ZIP_DEFLATED) as archive:
        for file_path in sorted(output_dir.rglob("*")):
            if file_path.is_file():
                archive.write(file_path, arcname=file_path.relative_to(output_dir))
                files_packed += 1
    return {
        "created": True,
        "zip_path": str(zip_path.relative_to(WORKSPACE_ROOT)),
        "files_packed": files_packed,
    }


