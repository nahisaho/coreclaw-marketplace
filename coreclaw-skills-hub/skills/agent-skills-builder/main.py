def _suite_requested(payload: dict, explicit_files: dict[str, str]) -> bool:
    structure = _stringify(payload.get("structure_type") or payload.get("structure") or payload.get("template_mode")).lower()
    if structure in {"suite", "suite-style", "group"}:
        return True
    if payload.get("suite_style") is True or payload.get("generate_group") is True:
        return True
    if payload.get("subskills") or payload.get("sub_skills") or payload.get("sub_agents"):
        return True
    return any(path == "group.json" or ("/" in path and not path.startswith("source/")) for path in explicit_files)


def _render_skill_json(skill_name: str, version: str, description: str, entrypoint: str) -> str:
    data = {
        "name": skill_name,
        "version": version,
        "description": description,
        "entrypoint": entrypoint,
    }
    return json.dumps(data, ensure_ascii=False, indent=2) + "\n"


def _render_group_json(group_title: str, description: str, icon: str, count: int) -> str:
    data = {
        "name": group_title,
        "description": description,
        "icon": icon,
        "count": count,
    }
    return json.dumps(data, ensure_ascii=False, indent=2) + "\n"


def _render_main_py(skill_name: str, version: str, description: str, capabilities: list[str] | None = None, status: str = "ready") -> str:
    skill_meta = {
        "name": skill_name,
        "version": version,
        "description": description,
        "capabilities": capabilities or ["artifact-output", "verification-loop"],
    }
    return f'''#!/usr/bin/env python3
"""Entrypoint for generated skill: {skill_name} ({version})."""

from __future__ import annotations

SKILL_META = {repr(skill_meta)}


def run(input_data: dict | None = None) -> dict:
    return {{
        "skill": SKILL_META["name"],
        "version": SKILL_META["version"],
        "description": SKILL_META["description"],
        "status": "{status}",
        "input": input_data or {{}},
    }}


if __name__ == "__main__":
    print(run())
'''


