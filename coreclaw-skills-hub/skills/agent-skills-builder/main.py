def _slugify(value: str) -> str:
    slug = re.sub(r"[^a-z0-9-]+", "-", value.lower()).strip("-")
    slug = re.sub(r"-+", "-", slug)
    return slug or "generated-skill"


def _target_format(payload: dict) -> tuple[str, str | None]:
    raw = _stringify(
        payload.get("target_format")
        or payload.get("format")
        or payload.get("skill_format")
        or payload.get("output_format")
    ).lower()
    if not raw:
        return "agent-skills", None
    normalized = SUPPORTED_TARGET_FORMAT_ALIASES.get(raw)
    if normalized:
        return normalized, None
    return "agent-skills", f"target_format '{raw}' is no longer supported; generated Agent Skills output instead."


def _agent_skill_name(value: str) -> str:
    return _slugify(value)[:64].strip("-") or "generated-skill"


def _artifact_dir(skill_name: str) -> Path:
    return ARTIFACT_ROOT / _slugify(skill_name)


def _display_name(skill_name: str) -> str:
    return " ".join(part.capitalize() for part in _slugify(skill_name).split("-"))


def _normalize_version(value: str | None) -> str:
    if not value:
        return "v0.1.0"
    normalized = value if value.startswith("v") else f"v{value}"
    if re.match(r"^v\d+\.\d+\.\d+$", normalized):
        return normalized
    return "v0.1.0"


def _stringify(value: object | None) -> str:
    return str(value).strip() if value is not None else ""


