def _suite_requested(payload: dict, explicit_files: dict[str, str]) -> bool:
    structure = _stringify(payload.get("structure_type") or payload.get("structure") or payload.get("template_mode")).lower()
    if structure in {"suite", "suite-style", "group"}:
        return True
    if payload.get("suite_style") is True or payload.get("generate_group") is True:
        return True
    if payload.get("subskills") or payload.get("sub_skills") or payload.get("sub_agents"):
        return True
    return any(path == "group.json" or ("/" in path and not path.startswith("source/")) for path in explicit_files)


def _yaml_scalar(value: str) -> str:
    if not value:
        return '""'
    if re.match(r"^[A-Za-z0-9._/-]+$", value):
        return value
    return json.dumps(value, ensure_ascii=False)


def _yaml_list(values: list[str], indent: int = 0) -> list[str]:
    prefix = " " * indent
    return [f"{prefix}- {_yaml_scalar(value)}" for value in values]


def _normalize_tools(value: object, default: list[str]) -> list[str]:
    if isinstance(value, list):
        tools = [_stringify(item) for item in value if _stringify(item)]
        return tools or list(default)
    if isinstance(value, str):
        parsed = [part.strip() for part in re.split(r"[,\n]", value) if part.strip()]
        return parsed or list(default)
    return list(default)


def _role_model_tier(role: str) -> str:
    if role == "orchestrator":
        return "premium"
    if role == "agent":
        return "standard"
    return "fast"


