def _slugify(value: str) -> str:
    slug = re.sub(r"[^a-z0-9-]+", "-", value.lower()).strip("-")
    return slug or "generated-skill"


def _artifact_dir(skill_name: str) -> Path:
    return ARTIFACT_ROOT / _slugify(skill_name)


def _display_name(skill_name: str) -> str:
    return " ".join(part.capitalize() for part in _slugify(skill_name).split("-"))


def _normalize_version(value: str | None) -> str:
    if not value:
        return "v0.1.0"
    normalized = value if value.startswith("v") else f"v{value}"
    if re.match(r"^v\d+\.\d+\.\d+$", normalized):
        return normalized
    return "v0.1.0"


def _stringify(value: object | None) -> str:
    return str(value).strip() if value is not None else ""


def _default_description(payload: dict, title: str) -> str:
    description = (
        payload.get("description")
        or payload.get("true_objective")
        or payload.get("surface_request")
        or payload.get("user_request")
    )
    if description:
        return _stringify(description)
    return f"{title} skill scaffold generated by agent-skills-builder."


def _default_group_description(group_title: str, root_description: str) -> str:
    if root_description:
        return root_description
    return f"Skills for {group_title.lower()} workflows."


def _default_orchestrator_description(group_title: str) -> str:
    return f"Coordinates the {group_title.lower()} workflow across specialized sub-skills with shared quality gates."


def _default_subskill_description(skill_title: str, group_title: str) -> str:
    return f"Executes the {skill_title.lower()} step inside the {group_title.lower()} workflow."


def _workflow_label(group_title: str) -> str:
    label = group_title.lower().strip()
    return label if label.endswith("workflow") else f"{label} workflow"


def _suite_profile(payload: dict) -> str:
    raw = _stringify(
        payload.get("suite_profile")
        or payload.get("profile")
        or payload.get("domain")
        or payload.get("workflow_type")
    ).lower()
    return SUITE_PROFILE_ALIASES.get(raw, "ops")


